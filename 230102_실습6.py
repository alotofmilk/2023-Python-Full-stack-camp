first_number = int(input("첫 번째 숫자를 입력해주세요 > "))
second_number = int(input("두 번째 숫자를 입력해주세요 > "))

print("더하기 연산 : {}".format(first_number + second_number))
print("빼기 연산 : {}".format(first_number - second_number))
print("곱하기 연산 : {}".format(first_number * second_number))
print("몫 연산 : {}".format(first_number // second_number))
print("나머지 연산 : {}".format(first_number % second_number))